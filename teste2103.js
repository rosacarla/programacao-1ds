// Definição das variáveis
let nome = 'Gabriel Mendonça';
let idade = 25;

// Impressão no console 
console.log(`Nome: ${nome}`);
console.log(`Idade: ${idade}`);

/* IMPORTANTE! 
Para interpolar (=incluir) variáveis em strings no JS, 
utilize template literals com crases (``). 
A sintaxe correta é `${variável}` dentro da string, 
por exemplo: ``console.log(`Nome: ${nome}`)``. */
