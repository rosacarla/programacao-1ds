// Inicializa a variável x com o valor 10
let x = 10;
console.log(`Valor inicial de x: ${x}`);

// Adiciona 5 ao valor atual de x (equivalente a x = x + 5)
x += 5;
console.log(`x += 5 → ${x} (10 + 5)`);

// Subtrai 3 do valor atual de x (equivalente a x = x - 3)
x -= 3;
console.log(`x -= 3 → ${x} (15 - 3)`);

// Multiplica o valor atual de x por 2 (equivalente a x = x * 2)
x *= 2;
console.log(`x *= 2 → ${x} (12 * 2)`);

// Divide o valor atual de x por 4 (equivalente a x = x / 4)
x /= 4;
console.log(`x /= 4 → ${x} (24 / 4)`);

// Atribui a x o resto da divisão por 5 (equivalente a x = x % 5)
x %= 5;
console.log(`x %= 5 → ${x} (6 % 5 = resto 1)`);

// Eleva x à potência de 3 (equivalente a x = x ** 3)
x **= 3;
console.log(`x **= 3 → ${x} (1 elevado a 3)`);
